package mx.com.ccplus.omr.model;

public class Coordinate {
    private double X;
    private double Y;

    public Coordinate() {
    }

    public Coordinate(double X, double Y) {
        this.X = X;
        this.Y = Y;
    }

    public double getX() {
        return X;
    }

    public void setX(double X) {
        this.X = X;
    }

    public double getY() {
        return Y;
    }

    public void setY(double Y) {
        this.Y = Y;
    }

    
    
    
    
}
